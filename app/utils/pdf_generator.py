import io
from datetime import datetime

import matplotlib
matplotlib.use('Agg')

import matplotlib.pyplot as plt
from reportlab.lib.pagesizes import A4
from reportlab.platypus import (
    SimpleDocTemplate, Paragraph, Spacer, Table, TableStyle, Image, HRFlowable
)
from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
from reportlab.lib import colors
from reportlab.lib.enums import TA_CENTER, TA_JUSTIFY

# -----------------------------
# GRAPH GENERATOR
# -----------------------------
def create_confidence_graph(confidence, threshold):
    fig, ax = plt.subplots(figsize=(6, 1.6))
    ax.barh(["Prediction Confidence"], [confidence], color="#1B4F72", height=0.6)
    ax.axvline(x=threshold, linestyle="--", color="#E74C3C", label=f"Threshold ({threshold}%)", linewidth=2)
    
    ax.set_xlim(0, 100)
    ax.set_xlabel("Probability Percentage (%)", fontsize=8, fontweight='bold')
    ax.spines['top'].set_visible(False)
    ax.spines['right'].set_visible(False)
    ax.legend(loc='upper right', frameon=True, fontsize=7)
    
    img_buffer = io.BytesIO()
    plt.tight_layout()
    plt.savefig(img_buffer, format="png", dpi=300)
    plt.close()
    img_buffer.seek(0)
    return img_buffer

# -----------------------------
# FIXED FOOTER
# -----------------------------
def draw_fixed_elements(canvas, doc):
    canvas.saveState()
    canvas.setStrokeColor(colors.HexColor("#1B4F72"))
    canvas.setLineWidth(1)
    canvas.line(45, 35, 550, 35)
    
    canvas.setFont("Helvetica-Oblique", 7)
    canvas.setFillColor(colors.grey)
    footer_text = "Generated by Flight Delay Predictor | ML Decision Support System | Optimized Final Version"
    canvas.drawString(45, 25, footer_text)
    canvas.drawRightString(550, 25, f"Report ID: {datetime.now().strftime('%Y%m%d%H%M')} | Page {doc.page}")
    canvas.restoreState()

# -----------------------------
# MAIN PDF GENERATOR
# -----------------------------
def generate_prediction_pdf(data):
    buffer = io.BytesIO()
    # Balanced margins to prevent overflow while allowing space for text expansion
    doc = SimpleDocTemplate(
        buffer, pagesize=A4, topMargin=30, bottomMargin=45, leftMargin=45, rightMargin=45
    )

    styles = getSampleStyleSheet()
    
    title_style = ParagraphStyle('Title', parent=styles['Heading1'], fontSize=20, 
                                textColor=colors.HexColor("#1B4F72"), alignment=TA_CENTER)
    subtitle_style = ParagraphStyle('Sub', parent=styles['Normal'], fontSize=8.5, 
                                   alignment=TA_CENTER, textColor=colors.black, spaceAfter=0)
    section_header = ParagraphStyle('Section', parent=styles['Heading2'], fontSize=11, 
                                   textColor=colors.HexColor("#1B4F72"), spaceBefore=6, spaceAfter=4)
    
    # GOLDEN RATIO FORMATTING: 
    # Leading is set to 12.5 (between the tight 11.5 and loose 13.5)
    # spaceAfter is set to 8 to provide breathing room without pushing to page 2
    body_style = ParagraphStyle('Body', parent=styles['Normal'], fontSize=9.5, 
                               leading=12.5, alignment=TA_JUSTIFY, spaceAfter=8)

    elements = []

    # --- Header ---
    elements.append(Paragraph("<b>Flight Delay Prediction Report</b>", title_style))
    elements.append(Paragraph("Intelligent Aviation Analytics System", subtitle_style))
    elements.append(Paragraph(f"Analysis Performed: {datetime.now().strftime('%d %B %Y at %H:%M')}", subtitle_style))
    elements.append(Spacer(1, 8))
    elements.append(HRFlowable(width="100%", thickness=1.5, color=colors.HexColor("#1B4F72"), spaceAfter=10))

    # --- Configuration ---
    elements.append(Paragraph("Flight Configuration Details", section_header))
    input_table = Table([
        ["Airlines Carrier", data["airline"], "Scheduled Month", data["month"]],
        ["Origin IATA", data["origin_airport"], "Day of Month", data["day"]],
        ["Destination IATA", data["destination_airport"], "Day of Week", data["day_of_week"]],
        ["Departure Hour", f"{data['departure_hour']}:00", "Route Distance", f"{data['distance']} miles"]
    ], colWidths=[110, 145, 110, 145])
    
    input_table.setStyle(TableStyle([
        ('BACKGROUND', (0, 0), (0, -1), colors.HexColor("#EBEDEF")),
        ('BACKGROUND', (2, 0), (2, -1), colors.HexColor("#EBEDEF")),
        ('GRID', (0, 0), (-1, -1), 0.5, colors.grey),
        ('FONTSIZE', (0, 0), (-1, -1), 8),
        ('BOTTOMPADDING', (0, 0), (-1, -1), 4),
        ('TOPPADDING', (0, 0), (-1, -1), 4),
    ]))
    elements.append(input_table)

    # --- Output ---
    elements.append(Paragraph("System Prediction Output", section_header))
    is_delayed = str(data["prediction"]).lower() == "delayed"
    primary_color = colors.HexColor("#C0392B") if is_delayed else colors.HexColor("#27AE60")
    secondary_color = colors.HexColor("#F2D7D5") if is_delayed else colors.HexColor("#D4EFDF")

    result_table = Table([
        ["Predicted Status", "Model Confidence", "Decision Threshold"],
        [data["prediction"].upper(), f"{data['confidence']}%", f"{data['threshold']}%"]
    ], colWidths=[168, 168, 168], rowHeights=[20, 32])
    
    result_table.setStyle(TableStyle([
        ('BACKGROUND', (0, 0), (0, 0), primary_color), 
        ('BACKGROUND', (1, 0), (1, 0), colors.HexColor("#21618C")), 
        ('BACKGROUND', (2, 0), (2, 0), colors.HexColor("#2E86C1")),
        ('BACKGROUND', (0, 1), (0, 1), secondary_color),
        ('BACKGROUND', (1, 1), (1, 1), colors.HexColor("#D1E8F7")),
        ('BACKGROUND', (2, 1), (2, 1), colors.HexColor("#D6EAF8")),
        ('TEXTCOLOR', (0, 0), (-1, 0), colors.white),
        ('ALIGN', (0, 0), (-1, -1), 'CENTER'),
        ('VALIGN', (0, 0), (-1, -1), 'MIDDLE'),
        ('FONTNAME', (0, 0), (-1, 1), 'Helvetica-Bold'),
        ('FONTSIZE', (0, 1), (-1, 1), 12),
        ('BOX', (0, 0), (-1, -1), 1, colors.white),
    ]))
    elements.append(result_table)

    # --- Graph ---
    elements.append(Paragraph("Probability Visualization", section_header))
    graph_buffer = create_confidence_graph(float(data["confidence"]), float(data["threshold"]))
    elements.append(Image(graph_buffer, width=480, height=130))

    # --- Model ---
    elements.append(Paragraph("Model Architecture & Performance", section_header))
    raw_size = str(data["dataset_size"]).replace(",", "")
    try:
        formatted_size = f"{int(float(raw_size)):,} samples"
    except:
        formatted_size = f"{data['dataset_size']} samples"

    model_table = Table([
        ["Primary Model", data["model_name"], "Accuracy Score", f"{str(data['accuracy']).replace('%', '')}%"],
        ["F1 Metric", data["f1_score"], "Training Data", formatted_size]
    ], colWidths=[110, 145, 110, 145])
    
    model_table.setStyle(TableStyle([
        ('BACKGROUND', (0, 0), (0, -1), colors.HexColor("#EBEDEF")),
        ('BACKGROUND', (2, 0), (2, -1), colors.HexColor("#EBEDEF")),
        ('GRID', (0, 0), (-1, -1), 0.5, colors.grey),
        ('FONTSIZE', (0, 0), (-1, -1), 8),
        ('BOTTOMPADDING', (0, 0), (-1, -1), 4),
        ('TOPPADDING', (0, 0), (-1, -1), 4),
    ]))
    elements.append(model_table)

    # --- ESSAY (Insights) ---
    elements.append(Paragraph("Technical Analysis & Insights", section_header))
    
    # Dynamic Essay Blocks
    p1 = f"""
    The AI Flight Delay Predictor has classified this specific route from <b>{data['origin_airport']}</b> to 
    <b>{data['destination_airport']}</b> as <b>{data['prediction'].upper()}</b>. This determination is rooted 
    in a longitudinal analysis of carrier performance during <b>Month {data['month']}</b>, weighted against a 
    robust baseline of <b>{formatted_size}</b> historical flight records. By evaluating the interaction between 
    seasonal demand and historical throughput at <b>{data['origin_airport']}</b>, the system identifies 
    underlying patterns that deviate from standard operational norms, providing a high-confidence forecast 
    that surpasses simple probabilistic averages.
    """
    
    p2 = f"""
    Our <b>{data['model_name']}</b> architecture indicates that <b>Departure Hour ({data['departure_hour']}:00)</b> 
    and <b>Route Distance ({data['distance']} miles)</b> serve as the primary high-weight features for this 
    prediction. The model assesses the risk of cascading delays, operational setbacks that accumulate over a 
    service day, by correlating scheduled timing with the specific performance metrics of <b>{data['airline']}</b>. 
    Because the calculated probability sits at <b>{data['confidence']}%</b>, the system effectively isolates 
    this flight's unique risk profile from generalized airport congestion, ensuring the prediction is specific 
    to the aircraft's scheduled service window.
    """
    
    p3 = f"""
    Based on the current <b>Decision Threshold of {data['threshold']}%</b>, this report serves as a critical 
    decision-support tool for ground operations and logistics planning. For flights predicted as 
    <b>{data['prediction'].upper()}</b>, the system recommends a tiered response: high-confidence delays should 
    trigger preemptive resource reallocation, while high-confidence on-time status allows for optimized gate 
    turn-around scheduling. Utilizing these insights enables airlines to mitigate the financial and logistical 
    impact of unforeseen schedule variances, directly improving passenger satisfaction and operational 
    efficiency across the <b>{data['airline']}</b> network.
    """
    
    elements.append(Paragraph(p1, body_style))
    elements.append(Paragraph(p2, body_style))
    elements.append(Paragraph(p3, body_style))

    # BUILD
    doc.build(elements, onFirstPage=draw_fixed_elements)
    buffer.seek(0)
    return buffer